// Helper to generate photo paths for a document
function getPhotos(id: number, count: number, mainExt: string, otherExts?: string[]): string[] {
  if (count === 1) {
    return [`/documents/${id}.${mainExt}`]
  }

  const photos: string[] = [`/documents/${id}-1.${mainExt}`]
  for (let i = 2; i <= count; i++) {
    const ext = otherExts?.[i - 2] || mainExt
    photos.push(`/documents/${id}-${i}.${ext}`)
  }
  return photos
}

